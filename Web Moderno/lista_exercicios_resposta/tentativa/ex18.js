function extenso(n) {
    switch (n) {
        case 1:
            console.log('um')
            break;
        case 2:
            console.log('dois')
            break;
        case 0:
            console.log('zero')

            break;

        default:
            console.log('fora intervalo')
            break;
    }
}

extenso(1)
extenso(2)
extenso(3)
extenso(0)